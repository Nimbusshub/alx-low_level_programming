/**
 * mul  - multiplies two integers
 * @a: first integer declared
 * @b: second integer declared
 * Description:  Program will multiply two integers
 * Return: number (The product of two integers)
 *
 */
int mul(int a, int b)
{
	int number;

	number = a * b;
	return (number);
}

